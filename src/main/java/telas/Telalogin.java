    private void btn_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_okActionPerformed
        String email = campo_email.getText();
        String senha = new String(campo_senha.getPassword());
    
    Usuarios usuario = null;
   
    try {
        usuario = CRUD.validarLogin(email, senha);
    } catch (SQLException ex) {
        logger.log(Level.SEVERE, "Erro ao validar login", ex);

    }

    if (usuario != null) {

        if (usuario.isAdm()) {
            Telaadm tela = new Telaadm(usuario);
            tela.setVisible(true);
        } else {
            Telalogado tela = new Telalogado(usuario);
            tela.setVisible(true);
        }

        this.dispose();

    } else {
        JOptionPane.showMessageDialog(this,
                "Email ou senha inv√°lidos",
                "Erro",
                JOptionPane.ERROR_MESSAGE);
    }

    
    }//GEN-LAST:event_btn_okActionPerformed

